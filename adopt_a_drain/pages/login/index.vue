<template>
  <section class="container">
    <Authenticate v-if="!authorized"/>

  </section>
</template>

<script>

import Authenticate from '@/components/Authenticate.vue'
export default {
  components: {
    Authenticate
  },
  computed: {
    authorized: function () {
      if ( !this.$store.state.authenticated ){ return false }
      return true
    }
  }
}
</script>
